#include "libft.h"

char	*ft_strncat(char *dest, char *src, int nb)
{
	int i;
	int dest_len;

	i = 0;
	dest_len = ft_strlen(dest);
	while (i < nb && src[i] != '\0')
	{
		dest[dest_len + i] = src[i];
		i++;
	}
	dest[dest_len + i] = '\0';
	return (dest);
}
